    public void lockAccessPrivilige() {
    	try {
        	concurrentLock.lock();    		
    	} catch (Exception e) {
    		logger.warn("lockAccessPrivilige failed.", e);
    	}
    }
    
    public void unLockAccessPrivilige() {
    	try {
        	if (concurrentLock.isLocked()) {
        		concurrentLock.unlock();    		
        	}
    	} catch (Exception e) {
    		logger.warn("unLockAccessPrivilige failed.", e);
    	}

    }
    
